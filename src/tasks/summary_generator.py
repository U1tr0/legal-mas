from __future__ import annotations

import json
from typing import Any, Dict

from src.llm.base import BaseLLMClient


SUMMARY_SYSTEM_PROMPT = """
Ты извлекаешь фабулу правонарушения из судебного документа.

Сначала найди в тексте конкретный фрагмент, где описаны действия лица, за которые его привлекают к ответственности.
Потом кратко переформулируй этот фрагмент в 1–2 предложениях.

Важно:
- Нельзя выдавать абстрактные фразы вроде "лицо совершило деяние" или "было совершено правонарушение".
- Если нет конкретного описания действий, времени, места, способа или сути нарушения, нужно вернуть null.
- Нельзя пересказывать процесс проверки, рассмотрения жалобы, выводы суда, мотивировку суда, резолютивную часть.
- Нельзя придумывать детали, которых нет в тексте.
- Нужно опираться только на реально найденный фрагмент текста.

Верни только JSON без пояснений.
"""

SUMMARY_USER_TEMPLATE = """
Извлеки фабулу из судебного документа.

Нужно найти именно тот фрагмент, где описано:
- что сделал человек или организация,
- за что его привлекают,
- в чем состояло нарушение.

Не нужно включать:
- кто рассматривал жалобу,
- кто проводил проверку,
- выводы суда,
- ссылки на нормы права,
- резолютивную часть.

Сначала найди подходящий фрагмент текста.
Потом на его основе составь краткую фабулу.

Если такого фрагмента нет или он не позволяет уверенно понять суть деяния, верни:
- "evidence": null
- "summary": null
- "status": "null"

Ответ строго в JSON:

{{
  "evidence": string | null,
  "summary": string | null,
  "status": "ok" | "null",
  "confidence": number
}}

Пример 1

Текст:
Основанием для привлечения Иванова И.И. к административной ответственности послужило то, что он 15 марта 2024 года управлял автомобилем в состоянии алкогольного опьянения.

Ответ:
{{
  "evidence": "Основанием для привлечения Иванова И.И. к административной ответственности послужило то, что он 15 марта 2024 года управлял автомобилем в состоянии алкогольного опьянения.",
  "summary": "15 марта 2024 года Иванов И.И. управлял автомобилем в состоянии алкогольного опьянения.",
  "status": "ok",
  "confidence": 0.96
}}

Пример 2

Текст:
Основанием для привлечения Семешкина Д.И. к административной ответственности послужило то, что он около 21 часа 00 минут 28 ноября 2023 года на почве неприязненных отношений ударил потерпевшую, причинив ей физическую боль.

Ответ:
{{
  "evidence": "Основанием для привлечения Семешкина Д.И. к административной ответственности послужило то, что он около 21 часа 00 минут 28 ноября 2023 года на почве неприязненных отношений ударил потерпевшую, причинив ей физическую боль.",
  "summary": "28 ноября 2023 года Семешкин Д.И. на почве неприязненных отношений совершил насильственные действия в отношении потерпевшей, причинив ей физическую боль.",
  "status": "ok",
  "confidence": 0.94
}}

Пример 3

Текст:
19 и 20 августа 2024 года антимонопольный орган провёл внеплановую проверку. В ходе проверки выявлено, что руководитель учреждения утвердил извещения о закупке с незаконными требованиями к участникам и без обязательных требований к качеству продукции.

Ответ:
{{
  "evidence": "руководитель учреждения утвердил извещения о закупке с незаконными требованиями к участникам и без обязательных требований к качеству продукции",
  "summary": "Руководитель учреждения утвердил извещения о закупке, в которых были установлены незаконные требования к участникам и не были соблюдены обязательные требования к качеству продукции.",
  "status": "ok",
  "confidence": 0.89
}}

Пример 4

Текст:
Верховный Суд Российской Федерации рассмотрел жалобу защитника и не нашёл оснований для отмены судебных актов.

Ответ:
{{
  "evidence": null,
  "summary": null,
  "status": "null",
  "confidence": 0.18
}}

Теперь обработай следующий текст.

Текст документа:
{document_text}
"""


class SummaryGenerator:
    """Извлекает короткую фактическую фабулу из судебного документа."""

    def __init__(self, llm_client: BaseLLMClient):
        self.llm_client = llm_client

    def generate_summary(self, full_text: str) -> Dict[str, Any]:
        if not isinstance(full_text, str) or not full_text.strip():
            return {
                "evidence": None,
                "summary": None,
                "status": "error",
                "confidence": 0.0,
                "model_name": self.llm_client.model_name,
                "raw_response": None,
            }

        user_prompt = SUMMARY_USER_TEMPLATE.format(document_text=full_text[:12000])

        raw_answer = self.llm_client.generate(
            system_prompt=SUMMARY_SYSTEM_PROMPT,
            user_prompt=user_prompt,
            temperature=0.0,
            max_tokens=800,
            response_format={"type": "json_object"},
        )

        try:
            parsed = json.loads(raw_answer)
        except json.JSONDecodeError:
            return {
                "evidence": None,
                "summary": None,
                "status": "error",
                "confidence": 0.0,
                "model_name": self.llm_client.model_name,
                "raw_response": raw_answer,
            }

        return {
            "evidence": parsed.get("evidence"),
            "summary": parsed.get("summary"),
            "status": parsed.get("status", "error"),
            "confidence": parsed.get("confidence"),
            "model_name": self.llm_client.model_name,
            "raw_response": raw_answer,
        }
